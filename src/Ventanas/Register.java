    public void activateLabels(boolean b, int n) {
        switch (n) {
            case 0:
                ErrorName.setVisible(b);
                break;
            case 1:
                ErrorLastName.setVisible(b);
                break;
            case 2:
                ErrorEmail.setVisible(b);
                break;
            case 3:
                ErrorPassword.setVisible(b);
                break;
            case 4:
                ErrorConfirmPassword.setVisible(b);
                break;
            case 5:
                ErrorCell.setVisible(b);
                break;

        }
    }

    public boolean thereEmptyFields() {

        String data[] = new String[6];
        boolean thereAre = false;
        data[0] = sName;
        data[1] = sLastName;
        data[2] = sEmail;
        data[3] = sPassword;
        data[4] = sConfirmPassword;
        data[5] = sCell;

        for (int i = 0; i < 6; i++) {
            if (data[i].isEmpty()) {
                thereAre = true;
                activateLabels(true, i);
            }
        }
        return thereAre;
    }

    public void activateLabelFocusLost(String s, JLabel label) {
        if (s.isEmpty()) {
            label.setVisible(true);
        } else {
            label.setVisible(false);
        }
    }

    public void validateCharacters(KeyEvent evt) {

        char c = evt.getKeyChar();

        if (!(Character.isLetter(c) || c == 32 || c == 8)) {
            evt.consume();

        } else {
            System.out.println(c);
        }
    }

    public void validateNumbers(KeyEvent evt) {
        char c = evt.getKeyChar();

        if (Character.isLetter(c)) {
            evt.consume();
        } else {
            System.out.println(c);
        }
    }

    public boolean validateEmail(String s) {
        Pattern pat = Pattern.compile("(\\W|^)[\\w.\\-]{0,25}@(hotmail|gmail||outlook)\\.com(\\W|$)");
        Matcher mat = pat.matcher(s);
        if (mat.find()) {
            return true;
        } else {
            return false;
        }
    }


