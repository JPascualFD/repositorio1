    private void BtnRegisterSuccesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegisterSuccesActionPerformed
        // TODO add your handling code here:
        getData();
        if (!thereEmptyFields()) {
            seteoLabel(false);

            if (sPassword.equals(sConfirmPassword)) {
                if (new Conexion().verificarExistenciaUsuario(sEmail)) {
                    //JOptionPane.showMessageDialog(null, "El email ya se encuentra registrado en la bd, ingresa otro");
                    ObAn.popUpErrorPanel.jLabel1.setIcon(new ImageIcon("src/Imagenes/EmailYaRegistrado.png"));
                    ObAn.popUpErrorPlantilla.setVisible(true);
                } else {
                    new Conexion().insertarCliente(sName, sLastName, sPassword, sEmail, sCell);
                    //JOptionPane.showMessageDialog(null, "Registro exitoso");
                    ObAn.popUpErrorPanel.jLabel1.setIcon(new ImageIcon("src/Imagenes/RegistroExitoso.png"));
                    ObAn.popUpErrorPlantilla.setVisible(true);
                    
                    this.seteoJtextField();
                    EmailValido.setVisible(false);
                    EmailInvalido.setVisible(false);
                    new CambiarPanel(Plantilla.jPanel1, ObAn.login);
                }
            } else {

            }

        } else {
            //JOptionPane.showMessageDialog(null, "Digita los datos obligatorios.");
            ObAn.popUpErrorPanel.jLabel1.setIcon(new ImageIcon("src/Imagenes/DigitaLosDatos.png"));
            ObAn.popUpErrorPlantilla.setVisible(true);
            System.out.println(sName);

        }

    }//GEN-LAST:event_BtnRegisterSuccesActionPerformed

