    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        int stock = Integer.parseInt(txtFieldCantidad.getText());

        if (inventario.getStock() != stock) {
            new SQL.Conexion().actualizarStock(ObAn.inventario.getEscuela(), ObAn.inventario.getCarrera(), ObAn.inventario.getTipo(), ObAn.inventario.getSexo(), ObAn.inventario.getTalla(), stock);
            txtFieldCantidad.setEditable(false);
            btnGuardar.setEnabled(false);

        } else {
            //JOptionPane.showMessageDialog(null, "Â¡Ingresa una cantidad diferente!");
            ObAn.popUpErrorPanel.jLabel1.setIcon(new ImageIcon("src/Imagenes/IngresaUnaCantidadDiferente.png"));
            ObAn.popUpErrorPlantilla.setVisible(true);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        ObAn.inventario.setTalla(32);
        ObAn.inventario.setSexo("Hombre");
        txtFieldCantidad.setText("");
        txtFieldCantidad.setEditable(false);
        btnGuardar.setEnabled(false);
        labelHombre.setVisible(true);
        labelChico.setVisible(true);
        labelMujer.setVisible(false);
        labelMediano.setVisible(false);
        labelGrande.setVisible(false);
        if (ObAn.inventario.getEscuela().equals("ITSPR")) {
            new CambiarPanel(Plantilla.jPanel1, ObAn.adminElegirTipoCamisa);
        } else if (ObAn.inventario.getEscuela().equals("UV")) {
            new CambiarPanel(Plantilla.jPanel1, ObAn.adminEelgirCarreraUV);
        } else if (ObAn.inventario.getEscuela().equals("TEPEYAC") || ObAn.inventario.getEscuela().equals("COBAEV")) {
            new CambiarPanel(Plantilla.jPanel1, ObAn.admin2);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

