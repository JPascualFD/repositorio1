        ObAn.colocarFoto(ObAn.catalogo1.labelCamisaML, 9);
        ObAn.colocarFoto(ObAn.catalogo1.labelPlayera, 15);
        
        //Cambiamos el color de los iconos junto con el RolloverIcon para mas placer.
        ObAn.login.cambiarIconMinimizar("src/Imagenes/Minimizar-1 (verdeblanco).png", "src/Imagenes/Minimizar-2 (verdeblanco).png");
