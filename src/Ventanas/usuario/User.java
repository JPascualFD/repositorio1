        ObAn.user.contadorClick = 0;
        
        //Establezco como valores predeterminados lo siguiente...
        ObAn.inventarioUsuario.setEscuela("ITSPR");
        ObAn.inventarioUsuario.setCarrera("Ing. en sistemas");
        ObAn.inventarioUsuario.setTipo("");
        
        //Coloco las fotos dentro del catalogo1 y a cada label correspóndiente.
